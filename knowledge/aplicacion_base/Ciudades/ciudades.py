"""Aplicación base del ERP KAVAC sobre ciudades"""

def que_muestra_la_pantalla_de_ciudades():
    return "Al pulsar el icono ciudades, el sistema presenta una pantalla que muestra los campos: país, estados y nombre, junto con los botones Cerrar, Cancelar y Guardar."

def que_campos_tiene_la_tabla_de_ciudades():
    return "También nos muestra una tabla de registros con los campos: estado, ciudad y acción, que a su vez presenta los botones Modificar y Eliminar."

def que_funcionalidades_tiene_la_tabla_de_ciudades():
    return "Esta tabla presenta un campo de búsqueda, el cual permite filtrar los registros, y a la vez un selector para indicar el número de registros a mostrar en la tabla."

def que_sucede_al_guardar_en_ciudades():
    return "Seguidamente, el sistema muestra que el registro se almaceno con éxito, y lo lista en la tabla de registros."

def que_boton_cierra_la_ventana_en_ciudades():
    return "Si se desea cerrar la ventana se pulsa cerrar."

def que_boton_restablece_el_formulario_en_ciudades():
    return "Si se requiere restablecer los campos del formulario se pulsa el botón Cancelar."

def como_se_modifica_un_registro_en_ciudades():
    return "Para modificar un registro presionamos el botón modificar registro, inmediatamente el sistema presenta la información ingresada previamente, se modifica la información que se requiera, y se pulsa Guardar."

def que_mensaje_muestra_al_actualizar_en_ciudades():
    return "Luego, el sistema muestra un mensaje de que la información se actualizo con éxito, y se actualiza la tabla de registro."

def como_se_elimina_un_registro_en_ciudades():
    return "Para eliminar un registro, pulsamos el botón eliminar para un registro de interés, inmediatamente el sistema presenta un mensaje de confirmación, si presionamos la opción Confirmar, el sistema elimina el registro y muestra un mensaje de que el registro se eliminó con éxito."
