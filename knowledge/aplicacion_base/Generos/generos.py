"""Aplicación base del ERP KAVAC sobre los generos"""


def que_muestra_la_pantalla_de_generos():
    return "Al pulsar el icono Géneros el sistema presenta una pantalla que muestra el campo nombre, junto con los botones Cerrar, Cancelar y Guardar."

def que_campos_tiene_la_tabla_de_generos():
    return "También muestra una tabla de registros con los campos: nombre y acción, que a su vez presenta los botones Modificar y Eliminar."

def que_funcionalidad_tiene_la_tabla_de_generos():
    return "Esta tabla presenta un campo de búsqueda, el cual permite filtrar los registros."

def que_sucede_al_guardar_en_generos():
    return "Seguidamente, el sistema muestra que el registro se almaceno con éxito, y lo lista en la tabla de registros."

def que_boton_cierra_la_ventana_en_generos():
    return "Si se desea cerrar la ventana se pulsa cerrar."

def que_boton_restablece_el_formulario_en_generos():
    return "Si se requiere restablecer los campos del formulario se pulsa el botón Cancelar."

def como_se_edita_un_registro_en_generos():
    return "Para editar un registro presionamos el botón modificar registro, inmediatamente el sistema presenta la información ingresada previamente, se modifica la información que se requiera, y se pulsa Guardar."

def que_mensaje_muestra_al_actualizar_en_generos():
    return "Luego, el sistema muestra un mensaje de que la información se actualizo con éxito, y se actualiza la tabla de registro."

def como_se_elimina_un_registro_en_generos():
    return "Para eliminar un registro, pulsamos el botón eliminar para un registro de interés, inmediatamente el sistema presenta un mensaje de confirmación, si presionamos la opción Confirmar, el sistema elimina el registro y muestra un mensaje de que el registro se eliminó con éxito."
