"""Aplicación base del ERP KAVAC sobre usuarios"""

def que_muestra_la_opcion_de_usuarios():
    return "En Usuarios: Al hacer clic sobre esta opción, el sistema nos muestra una tabla de registro con las opciones Usuario, Organización y una columna acción con los iconos de ver información, modificar y eliminar, además presenta un buscador para poder filtrar sobre la tabla los registros de interés."

def que_muestra_el_boton_ver_informacion_de_usuario():
    return "En el Botón Ver Información: se nos muestra una ventana con la información del usuario de su interés con los roles y permisos que posee el trabajador seleccionado, y el botón OK que permite cerrar la ventana."

def que_muestra_el_boton_modificar_de_usuario():
    return "En el Botón Modificar: se nos muestra un formulario que contiene los campos Institución: es un campo de selección, empleado, Nombre, Correo electrónico y Usuario, además de los roles y permisos donde como administrador del sistema se le puede asignar al trabajador."
