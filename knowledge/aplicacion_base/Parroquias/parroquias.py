"""Aplicación base del ERP KAVAC sobre parroquias"""


def que_muestra_la_pantalla_de_parroquias():
    return "Al pulsar el icono parroquias, el sistema presenta una pantalla que nos muestra los campos: país, estados, municipio, código y nombre, junto con los botones Cerrar, Cancelar y Guardar."

def que_campos_tiene_la_tabla_de_parroquias():
    return "También muestra una tabla de registros con los campos: estado, municipio, parroquia, código y acción, que a su vez presenta los botones Modificar y Eliminar."

def que_funcionalidades_tiene_la_tabla_de_parroquias():
    return "Esta tabla presenta el campo de búsqueda, el cual nos permite filtrar los registros, y también presenta un selector para indicar el número de registros a mostrar en la tabla."

def que_sucede_al_guardar_en_parroquias():
    return "Seguidamente, el sistema muestra que el registro se almaceno con éxito, y lo lista en la tabla de registros."

def que_boton_cierra_la_ventana_en_parroquias():
    return "Si se desea cerrar la ventana se pulsa cerrar."

def que_boton_restablece_el_formulario_en_parroquias():
    return "Si se requiere restablecer los campos del formulario se pulsa el botón Cancelar."

def como_se_elimina_un_registro_en_parroquias():
    return "Para eliminar un registro, pulsamos el botón eliminar para un registro de interés, inmediatamente el sistema presenta un mensaje de confirmación, si presionamos la opción Confirmar, el sistema elimina el registro y muestra un mensaje de que el registro se eliminó con éxito."
