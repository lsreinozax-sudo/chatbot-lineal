"""Aplicación base del ERP KAVAC sobre sectores economicos"""

def que_muestra_la_pantalla_de_sectores_economicos():
    return "Al pulsar el icono Sectores económicos, el sistema nos presenta una pantalla que muestra el campo nombre, junto con los botones Cerrar, Cancelar y Guardar."

def que_campos_tiene_la_tabla_de_sectores_economicos():
    return "También muestra una tabla de registros con los campos: nombre y acción, que a su vez presenta los botones Modificar y Eliminar."

def que_funcionalidad_tiene_la_tabla_de_sectores_economicos():
    return "Esta tabla presenta un campo de búsqueda, el cual nos permite filtrar los registros."

def que_sucede_al_guardar_en_sectores_economicos():
    return "Seguidamente, el sistema nos muestra que el registro se almaceno con éxito, y lo enlista en la tabla de registros."

def que_boton_cierra_la_ventana_en_sectores_economicos():
    return "Si deseamos cerrar la ventana presionamos el botón cerrar."

def que_boton_restablece_el_formulario_en_sectores_economicos():
    return "Si requerimos restablecer los campos del formulario procedemos a pulsar el botón Cancelar."

def como_se_edita_un_registro_en_sectores_economicos():
    return "Para editar un registro, presionamos el botón modificar para un registro de interés, inmediatamente el sistema nos presenta la información ingresada previamente, modificamos la información que se requiera, y se pulsa Guardar."

def que_mensaje_muestra_al_actualizar_en_sectores_economicos():
    return "Luego, el sistema nos muestra el mensaje de que la información se actualizo con éxito, y renueva la tabla de registro."

def como_se_elimina_un_registro_en_sectores_economicos():
    return "Para eliminar un registro, pulsamos el botón eliminar para un registro de interés, inmediatamente el sistema nos presenta un mensaje de confirmación, si presionamos la opción Confirmar, el sistema elimina el registro y muestra un mensaje de que el registro se eliminó con éxito."
