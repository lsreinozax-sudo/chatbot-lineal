"""Aplicación base del ERP KAVAC sobre organizaciones"""

def que_muestra_la_pantalla_de_tipo_de_organizaciones():
    return "Al pulsar el icono tipo de organizaciones, el sistema presenta una pantalla que nos muestra los campos: Acrónimo y nombre, junto con los botones Cerrar, Cancelar y Guardar."

def que_campos_tiene_la_tabla_de_tipo_de_organizaciones():
    return "También muestra una tabla de registros con los campos: acrónimo, nombre y acción, que a su vez presenta los botones Modificar y Eliminar."

def que_funcionalidades_tiene_la_tabla_de_tipo_de_organizaciones():
    return "Esta tabla presenta un campo de búsqueda, el cual nos permite filtrar los registros, y un selector para indicar el número de registros a mostrar en la tabla."

def que_sucede_al_guardar_en_tipo_de_organizaciones():
    return "Seguidamente, el sistema nos muestra que el registro se almaceno con éxito, y lo enlista en la tabla de registros."

def que_boton_cierra_la_ventana_en_tipo_de_organizaciones():
    return "Si deseamos cerrar la ventana se pulsa cerrar."

def que_boton_restablece_el_formulario_en_tipo_de_organizaciones():
    return "Si requerimos restablecer los campos del formulario se pulsa el botón Cancelar."

def como_se_edita_un_registro_en_tipo_de_organizaciones():
    return "Para editar un registro, presionamos el botón modificar para un registro de interés, inmediatamente el sistema nos presenta la información ingresada previamente, se modifica la información que requerimos, y se pulsa Guardar."

def que_mensaje_muestra_al_actualizar_en_tipo_de_organizaciones():
    return "Luego, el sistema nos muestra el mensaje de que la información se renovó con éxito, y se actualiza la tabla de registro."

def como_se_elimina_un_registro_en_tipo_de_organizaciones():
    return "Para eliminar un registro, se pulsa el botón eliminar para un registro de interés, inmediatamente el sistema presenta un mensaje de confirmación, si el usuario pulsa la opción Confirmar, el sistema elimina el registro y muestra mensaje de que el registro se eliminó con éxito."
